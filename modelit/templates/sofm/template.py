# =========================================================
# KOHONEN SELF-ORGANIZING MAP (SOM) / COMPETITIVE LEARNING
# Implementation from Scratch
# =========================================================


# =========================================================
# IMPORTS
# =========================================================

import numpy as np


# =========================================================
# DATASET DEFINITION (INPUTS & INITIAL WEIGHTS)
# =========================================================

x = np.array([
    [1, 0, 1, 1],
    [0, 0, 0, 1],
    [1, 0, 0, 1],
    [1, 1, 1, 0]
])

w = np.array([
    [0.9, 0.3, 0.1, 0.2],
    [0.1, 0.6, 0.2, 0.1]
])


# =========================================================
# HYPERPARAMETERS
# =========================================================

learning_rate = 0.1

epochs = 1


# =========================================================
# TRAINING SOM MODEL
# =========================================================

for epoch in range(epochs):

    print(f"\n=================================================")
    print(f"EPOCH {epoch + 1}")
    print("=================================================")

    print("Current Learning Rate:", learning_rate)

    for i in range(len(x)):

        print(f"\n-------------------------------------------------")
        print(f"Input Sample {i + 1}: {x[i]}")
        print(f"-------------------------------------------------")


        # =========================================================
        # EUCLIDEAN DISTANCE CALCULATION
        # =========================================================

        distances = ((w - x[i]) ** 2).sum(axis=1)

        print("Calculated Distances to Nodes:", distances)


        # =========================================================
        # FIND BEST MATCHING UNIT (BMU)
        # =========================================================

        bmu = np.argmin(distances)

        print("Winning Node (BMU Index):", bmu + 1)


        # =========================================================
        # WEIGHT UPDATE RULE
        # =========================================================

        w[bmu] = w[bmu] + learning_rate * (x[i] - w[bmu])

        print("\nUpdated Weights Matrix:\n", w)


    # =========================================================
    # DECAY LEARNING RATE
    # =========================================================

    learning_rate *= 0.5